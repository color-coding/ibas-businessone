package org.colorcoding.ibas.bobas.businessone.data;

public class B1Exception extends Exception {

	private static final long serialVersionUID = 8961742388209295974L;

	public B1Exception() {
		super();
	}

	public B1Exception(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public B1Exception(String arg0) {
		super(arg0);
	}

	public B1Exception(Throwable arg0) {
		super(arg0);
	}

}
